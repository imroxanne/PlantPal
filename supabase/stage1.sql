-- Stage 1: Каталог растений + коллекция пользователя
-- Запустить в Supabase SQL Editor

-- Таблица каталога растений
CREATE TABLE plants (
  id                    uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  common_name           text NOT NULL,
  latin_name            text,
  category              text,
  description           text,
  watering_interval_days integer NOT NULL DEFAULT 7,
  light                 text,
  humidity              text,
  temperature           text,
  soil                  text,
  fertilizing           text,
  repotting             text,
  toxicity              text,
  image_url             text,
  status                text NOT NULL DEFAULT 'published',
  created_at            timestamptz NOT NULL DEFAULT now()
);

-- Коллекция пользователя
CREATE TABLE user_plants (
  id              uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id         uuid NOT NULL REFERENCES users(id),
  plant_id        uuid NOT NULL REFERENCES plants(id),
  nickname        text,
  photo_url       text,
  purchase_date   date,
  last_watered    timestamptz,
  last_fertilized timestamptz,
  last_repotted   timestamptz,
  is_archived     boolean NOT NULL DEFAULT false,
  created_at      timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX idx_user_plants_user_id ON user_plants(user_id);
CREATE INDEX idx_plants_status ON plants(status);

